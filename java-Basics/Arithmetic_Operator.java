package com.basic.java;

public class Arithmetic_Operator {
    public static void main(String[] args) {
        int a, b;
        a = 100;
        b = 200;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(b/a);
        System.out.println(a*b);
    }

}
